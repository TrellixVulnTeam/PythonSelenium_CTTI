s="chandan Ghosh"
print(s.lower())# converts all the characters into lowercased
print(s.upper())# converts all the characters into uppercase
print(s.title()) # converts the first character of each word to upper case
print(s.capitalize())#converts the first character of the string to upper case
print(s.swapcase())# converts the lower case to uppercase and vice versa

#check starting and ending part of the stringsplit
print(s.startswith("chan"))
print(s.endswith("chan"))